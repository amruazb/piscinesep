#include <unistd.h>

void	ps(char *c)
{
	if (*c)
	{
		write(1, c, 1);
		ps(c + 1);
	}
}

int	main(int ac, char **av)
{
	(void)ac;
	ps(av[0]);
	ps("\n");
}
