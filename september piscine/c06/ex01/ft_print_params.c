#include <unistd.h>

void	ps(char *s)
{
	if (*s)
	{
		write(1, s, 1);
		ps(s + 1);
	}
}

int	main(int ac, char **av)
{
	int	i;

	i = 1;
	while (ac > i)
	{
		ps(av[i++]);
		ps("\n");
	}
}
